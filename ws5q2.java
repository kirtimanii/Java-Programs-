
/**
 * Program to display square of first 10 natural number using while loop
 *
 * K
 * v1.0
 */
public class ws5q2
{
    public static void main(String[] args) 
    {
        int i = 1;
        while (i <= 10)  // while loop
        {
            System.out.println(i*i);
            i++;
        }
    
    }
}