public class tutorial5
{
    public static void main(String[]args)
    {
        
        int sum=0;
        int i;
        
        for(i=1;i<=10;i++)  // for loop
        {
            sum=sum+i;
        }
        
        System.out.println(sum);
        
        int n=1;
        
        while(n<=5)  // while loop
        {
            System.out.println(n);
            n++;
        }
        
        
        int h=1;
        
        do  // do while loop
        {
            System.out.println(h);
            h++;
        } while(h<1);
        

    }
}